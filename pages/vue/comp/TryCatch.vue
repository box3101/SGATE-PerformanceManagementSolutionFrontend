<script setup>
  /*
      Try-Catch 문이란?
      - 사전단어 : Try(시도하다) + Catch(잡다, 포착하다)
      - 프로그램에서 '에러가 발생할 수 있는 코드'를 안전하게 실행하는 방법
      - "이 코드를 시도해보고, 문제가 생기면 잡아서 처리해줘"라는 의미

      [ 기본 구조 ]
      try {
        // 에러가 발생할 수 있는 코드
      } catch (error) {
        // 에러가 발생했을 때 실행할 코드
      } finally {
        // 성공하든 실패하든 항상 실행되는 코드 (선택사항)
      }

      [ 실행 흐름 ]
      1. try 블록의 코드를 시행
      2. 에러가 없으면 -> catch 블록은 건너 뛰고 finally 실행
      3. 에러가 발생하면 -> 즉시 catch 블록으로 이동해서 에러 처리
      4. finally 블록은 항상 실행(있다면)
   */
</script>

